interface ResultPanelProps {
  result: string | null
  loading: boolean
  placeholder?: string
  className?: string
  citations?: Citation[]
  confidence?: ConfidenceLevel
  onClear?: () => void
  viewerRef?: React.RefObject<DocumentViewerRef>
}

// ── Markdown → styled HTML for DOCX / plain download ─────────────────────────

function markdownToHTMLBody(md: string, highlights: Highlight[]): string {
  let html = md
    .replace(/&/g, '&amp;').replace(/</g, '&lt;').replace(/>/g, '&gt;')

  // Apply user highlights before other conversions
  for (const h of highlights) {
    const escaped = h.text.replace(/[.*+?^${}()|[\]\\]/g, '\\$&')
    const { r, g, b } = parseRgba(h.color)
    html = html.replace(
      new RegExp(escaped, 'gi'),
      `<mark style="background:rgba(${r},${g},${b},0.55);border-radius:3px;padding:1px 3px">$&</mark>`
    )
  }

  html = html
    .replace(/^### (.+)$/gm, '<h3>$1</h3>')
    .replace(/^## (.+)$/gm, '<h2>$1</h2>')
    .replace(/^# (.+)$/gm, '<h1>$1</h1>')
    .replace(/\*\*(.+?)\*\*/g, '<strong>$1</strong>')
    .replace(/\*(.+?)\*/g, '<em>$1</em>')
    .replace(/`(.+?)`/g, '<code>$1</code>')
    .replace(/^[-*•] (.+)$/gm, '<li>$1</li>')
    .replace(/^> (.+)$/gm, '<blockquote>$1</blockquote>')
    .replace(/^---+$/gm, '<hr>')

  return html
}

function parseRgba(rgba: string): { r: number; g: number; b: number } {
  const m = rgba.match(/rgba?\((\d+),\s*(\d+),\s*(\d+)/)
  return m ? { r: +m[1], g: +m[2], b: +m[3] } : { r: 253, g: 224, b: 71 }
}

function downloadDOCX(result: string, highlights: Highlight[]) {
  const body = markdownToHTMLBody(result, highlights)
  const html = `<html xmlns:o='urn:schemas-microsoft-com:office:office'
  xmlns:w='urn:schemas-microsoft-com:office:word'
  xmlns='http://www.w3.org/TR/REC-html40'>
<head><meta charset='utf-8'><style>
  body { font-family: Calibri, sans-serif; font-size: 11pt; line-height: 1.7; margin: 1in; color: #1a1a1a; }
  h1 { font-size: 18pt; font-weight: bold; color: #005ac2; margin: 18pt 0 8pt; }
  h2 { font-size: 14pt; font-weight: bold; color: #005ac2; margin: 14pt 0 6pt; border-bottom: 1pt solid #ccc; padding-bottom: 3pt; }
  h3 { font-size: 12pt; font-weight: bold; margin: 12pt 0 4pt; }
  p { margin: 0 0 8pt; }
  li { margin: 3pt 0; }
  ul { padding-left: 18pt; }
  blockquote { margin: 8pt 0 8pt 18pt; color: #444; font-style: italic; border-left: 3pt solid #005ac2; padding-left: 8pt; }
  code { font-family: Courier New; font-size: 9pt; background: #f3f3f3; }
  strong { font-weight: bold; }
  em { font-style: italic; }
  hr { border: none; border-top: 1pt solid #ccc; margin: 12pt 0; }
  table { border-collapse: collapse; width: 100%; margin: 8pt 0; }
  th { background: #e8f0ff; font-weight: bold; padding: 5pt 8pt; border: 0.5pt solid #c0c8d8; }
  td { padding: 4pt 8pt; border: 0.5pt solid #ddd; }
</style></head>
<body>
<p style="color:#888;font-size:8pt;margin-bottom:20pt">Generated by LegalDoc AI · For informational purposes only</p>
<p>${body}</p>
</body></html>`

  const blob = new Blob([html], { type: 'application/msword' })
  const url = URL.createObjectURL(blob)
  const a = document.createElement('a')
  a.href = url
  a.download = 'legaldoc-output.doc'
  a.click()
  URL.revokeObjectURL(url)
}

function downloadTXT(result: string) {
  const blob = new Blob([result], { type: 'text/plain;charset=utf-8' })
  const url = URL.createObjectURL(blob)
  const a = document.createElement('a')
  a.href = url
  a.download = 'legaldoc-output.txt'
  a.click()
  URL.revokeObjectURL(url)
}

// ── Component ─────────────────────────────────────────────────────────────────

export function ResultPanel({
  result,
  loading,
  placeholder,
  className,
  citations,
  confidence,
  onClear,
  viewerRef,
}: ResultPanelProps) {
  const [copied, setCopied] = useState(false)
  const [downloadOpen, setDownloadOpen] = useState(false)
  const [highlights, setHighlights] = useState<Highlight[]>([])
  const internalRef = useRef<DocumentViewerRef>(null)
  const activeRef = viewerRef ?? internalRef

  const copy = async () => {
    if (!result) return
    await navigator.clipboard.writeText(result)
    setCopied(true)
    setTimeout(() => setCopied(false), 2000)
  }

  const addHighlight = (h: Highlight) =>
    setHighlights(prev => [...prev, h])

  const clearHighlights = () => setHighlights([])

  const handleClearOutput = () => {
    setHighlights([])
    onClear?.()
  }

  const handlePDFDownload = async () => {
    if (!result) return
    setDownloadOpen(false)
    try {
      const { generatePDF } = await import('@/lib/pdf-generator')
      await generatePDF(result, highlights, 'LegalDoc Analysis')
    } catch (e) {
      console.error('PDF generation failed', e)
    }
  }

  return (
    <div
      className={cn(
        'rounded-[18px] macos-card flex flex-col overflow-hidden min-h-[560px]',
        className
      )}
    >
      {/* Panel header */}
      <div className="flex items-center justify-between px-5 py-3.5 border-b border-separator flex-shrink-0 bg-surface/70">
        <div className="flex items-center gap-2">
          <span
            className="w-2 h-2 rounded-full bg-accent"
            style={{ animation: loading ? 'pulse 1s ease-in-out infinite' : 'none' }}
          />
          <span className="text-xs font-medium text-muted uppercase tracking-wider">Review Output</span>
          {confidence && result && <ConfidenceBadge level={confidence} className="ml-2" />}
        </div>

        {result && (
          <div className="flex items-center gap-1">
            {/* Clear output */}
            <button
              onClick={handleClearOutput}
              className="inline-flex items-center gap-1.5 text-[11px] h-7 px-2.5 rounded-md text-muted hover:text-red-400 hover:bg-red-500/10 transition-all border border-transparent hover:border-red-500/20"
              title="Clear output"
            >
              <Trash2 className="w-3.5 h-3.5" />
              Clear
            </button>

            {/* Download dropdown */}
            <div className="relative">
              <button
                onClick={() => setDownloadOpen(v => !v)}
                onBlur={() => setTimeout(() => setDownloadOpen(false), 150)}
                className="inline-flex items-center gap-1.5 text-[11px] h-7 px-2.5 rounded-md bg-white/[0.04] border border-separator text-muted hover:text-foreground hover:border-card-border-hover transition-all font-medium"
              >
                <Download className="w-3.5 h-3.5" />
                Download
                <ChevronDown className="w-3 h-3 opacity-60" />
              </button>
              <AnimatePresence>
                {downloadOpen && (
                  <motion.div
                    initial={{ opacity: 0, y: 4, scale: 0.97 }}
                    animate={{ opacity: 1, y: 0, scale: 1 }}
                    exit={{ opacity: 0, y: 4, scale: 0.97 }}
                    transition={{ duration: 0.12 }}
                    className="absolute right-0 top-full mt-1.5 w-44 rounded-xl border border-separator bg-[#141414] shadow-xl overflow-hidden z-20"
                  >
                    {[
                      { label: 'PDF (Beautified)', icon: File, action: handlePDFDownload },
                      { label: 'Word (.doc)', icon: FileText, action: () => { downloadDOCX(result, highlights); setDownloadOpen(false) } },
                      { label: 'Plain Text (.txt)', icon: FileText, action: () => { downloadTXT(result); setDownloadOpen(false) } },
                    ].map(({ label, icon: Icon, action }) => (
                      <button
                        key={label}
                        onClick={action}
                        className="w-full flex items-center gap-2.5 px-3.5 py-2.5 text-[12px] text-foreground/80 hover:bg-white/[0.06] hover:text-foreground transition-colors text-left"
                      >
                        <Icon className="w-3.5 h-3.5 text-muted flex-shrink-0" />
                        {label}
                      </button>
                    ))}
                  </motion.div>
                )}
              </AnimatePresence>
            </div>

            <Button variant="ghost" size="sm" onClick={copy} className="gap-1.5 text-xs h-7 px-2">
              {copied ? (
                <CheckCheck className="w-3.5 h-3.5 text-green-400" />
              ) : (
                <Copy className="w-3.5 h-3.5" />
              )}
              {copied ? 'Copied' : 'Copy'}
            </Button>
          </div>
        )}
      </div>

      {/* Content */}
      <div className="flex-1 overflow-y-auto p-6">
        <AnimatePresence mode="wait">
          {loading ? (
            <motion.div
              key="loading"
              initial={{ opacity: 0 }}
              animate={{ opacity: 1 }}
              exit={{ opacity: 0 }}
              className="h-full flex flex-col items-center justify-center gap-5 py-16 text-center"
            >
              <div className="relative">
                <div className="w-14 h-14 rounded-[14px] bg-white/[0.04] border border-card-border-hover flex items-center justify-center">
                  <Scale className="w-6 h-6 text-accent" />
                </div>
                <Loader2 className="absolute -top-1 -right-1 w-5 h-5 text-accent animate-spin" />
              </div>
              <div>
                <p className="text-sm font-medium mb-1">Building legal review…</p>
                <p className="text-xs text-muted">Usually takes 5–20 seconds</p>
              </div>
              <div className="w-full max-w-xs space-y-2">
                {[75, 55, 85, 45].map((w, i) => (
                  <motion.div
                    key={i}
                    className="h-2 rounded-full bg-white/[0.055]"
                    style={{ width: `${w}%` }}
                    animate={{ opacity: [0.3, 0.7, 0.3] }}
                    transition={{ duration: 1.4, delay: i * 0.18, repeat: Infinity }}
                  />
                ))}
              </div>
            </motion.div>
          ) : result ? (
            <motion.div
              key="result"
              initial={{ opacity: 0, y: 8 }}
              animate={{ opacity: 1, y: 0 }}
              transition={{ duration: 0.3 }}
              className="space-y-6"
            >
              <DocumentViewer
                ref={activeRef}
                content={result}
                highlights={highlights}
                onAddHighlight={addHighlight}
                onClearHighlights={clearHighlights}
              />
              {citations && citations.length > 0 && (
                <CitationList citations={citations} className="pt-4 border-t border-separator" />
              )}
            </motion.div>
          ) : (
            <motion.div
              key="empty"
              initial={{ opacity: 0 }}
              animate={{ opacity: 1 }}
              className="h-full flex flex-col items-center justify-center gap-3 text-center py-16"
            >
              <div className="w-14 h-14 rounded-[14px] bg-white/[0.035] border border-card-border flex items-center justify-center">
                <Scale className="w-6 h-6 text-muted" />
              </div>
              <div>
                <p className="text-sm text-muted">{placeholder || 'Output will appear here'}</p>
                <p className="text-xs text-muted-dark mt-1">Submit your input to get started</p>
              </div>
            </motion.div>
          )}
        </AnimatePresence>
      </div>
    </div>
  )
}
